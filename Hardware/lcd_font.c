static Status_t LCD_DecodeUtf8(const char *text, uint32_t *codePoint,
                               uint8_t *length)
{
    uint8_t first;
    uint8_t second;
    uint8_t third;

    if ((text == NULL) || (codePoint == NULL) || (length == NULL)) {
        return STATUS_INVALID_PARAM;
    }
    first = (uint8_t)text[0];
    if (first < 0x80U) {
        *codePoint = first;
        *length = 1U;
        return STATUS_OK;
    }
    second = (uint8_t)text[1];
    if ((first >= 0xC2U) && (first <= 0xDFU) &&
        ((second & 0xC0U) == 0x80U)) {
        *codePoint = ((uint32_t)(first & 0x1FU) << 6U) |
                     (uint32_t)(second & 0x3FU);
        *length = 2U;
        return STATUS_OK;
    }
    if ((first < 0xE0U) || (first > 0xEFU)) {
        return STATUS_INVALID_PARAM;
    }
    third = (uint8_t)text[2];
    if (((second & 0xC0U) != 0x80U) || ((third & 0xC0U) != 0x80U) ||
        ((first == 0xE0U) && (second < 0xA0U)) ||
        ((first == 0xEDU) && (second >= 0xA0U))) {
        return STATUS_INVALID_PARAM;
    }
    *codePoint = ((uint32_t)(first & 0x0FU) << 12U) |
                 ((uint32_t)(second & 0x3FU) << 6U) |
                 (uint32_t)(third & 0x3FU);
    *length = 3U;
    return STATUS_OK;
}
static const uint8_t *LCD_FindChinese16(const char *text)
{
    uint8_t utf8[3];
    uint16_t index;

    utf8[0] = (uint8_t)text[0];
    utf8[1] = (uint8_t)text[1];
    utf8[2] = (uint8_t)text[2];
    for (index = 0U; index < ARRAY_SIZE(g_font16); ++index) {
        if ((g_font16[index].utf8[0] == utf8[0]) &&
            (g_font16[index].utf8[1] == utf8[1]) &&
            (g_font16[index].utf8[2] == utf8[2])) {
            return g_font16[index].bitmap;
        }
    }
    return NULL;
}

Status_t LCD_ShowChinese16(uint16_t x, uint16_t y, const char *text,
                           uint16_t color, uint16_t background)
{
    uint8_t pixels[16U * 16U * 2U];

    if (text == NULL) { return STATUS_INVALID_PARAM; }
    while (*text != '\0') {
        uint32_t codePoint;
        uint8_t length;
        Status_t status = LCD_DecodeUtf8(text, &codePoint, &length);
        if (status != STATUS_OK) { return status; }

        if (codePoint < 0x80U) {
            char ascii[2] = {(char)codePoint, '\0'};
            status = LCD_ShowString(x, y, ascii, color, background);
            if (status != STATUS_OK) { return status; }
            x = (uint16_t)(x + 6U);
            text += length;
            continue;
        }

        if ((x >= LCD_WIDTH) || (y >= LCD_HEIGHT) ||
            (16U > (LCD_WIDTH - x)) || (16U > (LCD_HEIGHT - y))) {
            return STATUS_OVERFLOW;
        }
        {
            const uint8_t *bitmap = LCD_FindChinese16(text);
            uint16_t output = 0U;
            uint8_t row;
            uint8_t column;
            if (bitmap == NULL) { return STATUS_INVALID_PARAM; }
            for (row = 0U; row < 16U; ++row) {
                for (column = 0U; column < 16U; ++column) {
                    bool on = (bitmap[(uint16_t)row * 2U +
                                       (column / 8U)] &
                               (uint8_t)(0x80U >> (column % 8U))) != 0U;
                    uint16_t pixel = on ? color : background;
                    pixels[output++] = (uint8_t)(pixel >> 8U);
                    pixels[output++] = (uint8_t)pixel;
                }
            }
            status = LCD_WriteRegion(x, y, 16U, 16U,
                                     pixels, sizeof(pixels));
            if (status != STATUS_OK) { return status; }
        }
        x = (uint16_t)(x + 16U);
        text += length;
    }
    return STATUS_OK;
}
